def check_bingo(card_data, called_set):
    letters = ['B', 'I', 'N', 'G', 'O']
    grid = []
    for row in range(5):
        r = []
        for l in letters:
            val = card_data[l][row]
            r.append(True if val == 'FREE' else val in called_set)
        grid.append(r)
    for row in grid:
        if all(row):
            return True
    for col in range(5):
        if all(grid[r][col] for r in range(5)):
            return True
    if all(grid[i][i] for i in range(5)):
        return True
    if all(grid[i][4 - i] for i in range(5)):
        return True
    if grid[0][0] and grid[0][4] and grid[4][0] and grid[4][4]:
        return True
    return False
